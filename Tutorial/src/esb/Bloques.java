public class Bloques {
	
	/*
	 * Bloques de código
	 * Los bloques de código están compuestos por 1 o más sentencias y se encuentran delimitados por
	 * los símbolos de llaves { y }, y se emplean en diferentes estructuras del lenguaje como las funciones
	 * las estructuras de control como los if, if...else, switch, estructuras iterativas como while, for etc. 
	 * 
	 * */
	
	public static void main(String[] args) { // <-- Inicio de bloque de código
		
		System.out.println("Esta es una sentencia");

	}// <-- Fin de Bloque de codigo
	
}
